// 3. Find the summation of all odd numbers between 1 to 500 and print them on teh console log.
let sumOddNumbers = 0;
for (let i = 1; i <= 500; i++) {
    if (i % 2 !== 0) {
        sumOddNumbers += i;
    }
}
console.log(`The summation of all odd numbers between 1 to 500 is: ${sumOddNumbers}`);

