/* Biwise Doubling
- [ ] Create a variable `count` and assign  a value, say, 5.
- [ ] Now use the Bitwise shift operator to make the number double.
- [ ] Print it on the console.
*/
const count = 5;
const doubledCount = count << 1; // Left shift operator to double the value
console.log(`The doubled value of ${count} is ${doubledCount}.`);